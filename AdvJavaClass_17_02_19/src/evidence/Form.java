    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:

        //For name
        txtName.getText();
        String name = txtName.getText();
        System.out.println(name);
        //For age
        Integer.parseInt(txtAge.getText());
        int age = Integer.parseInt(txtAge.getText());
        System.out.println(age);
        //For Email
        txtEmail.getText();
        String email = txtEmail.getText();
        System.out.println(email);
        //For Gender
        String gender = "";
        if (rMale.isSelected() == true) {
            gender = rMale.getText();
        } else if (rFemale.isSelected() == true) {
            gender = rFemale.getText();
        }
        System.out.println(gender);
        //For Complete course
        String cc = "";
        if (chkModule1.isSelected()) {
            cc += chkModule1.getText();
        }
        if (chkModule2.isSelected()) {
            cc += chkModule2.getText();
        }
        if (chkModule3.isSelected()) {
            cc += chkModule3.getText();
        }
        if (chkModule4.isSelected()) {
            cc += chkModule4.getText();
        }
        if (chkModule5.isSelected()) {
            cc += chkModule5.getText();
        }
        System.out.println(cc);
        //For round
        String round = cmbRound.getItemAt(cmbRound.getSelectedIndex());
        System.out.println(round);
        //For message
        txtAreaMsg.getText();
        String msg = txtAreaMsg.getText();
        System.out.println(msg);
        
        //For insert into database
        Student student = new Student(name, age, email, gender, cc, round, msg);
        CommonDAO obj = new StudentService();
        obj.save(student);

        //For Show in table
        DefaultTableModel model = (DefaultTableModel) tblDisplay.getModel();
        Object[] row = new Object[7];
        row[0] = name;
        row[1] = age;
        row[2] = email;
        row[3] = gender;
        row[4] = cc;
        row[5] = round;
        row[6] = msg;

        model.addRow(row);
    }//GEN-LAST:event_btnAddActionPerformed

